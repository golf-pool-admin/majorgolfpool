        Martin Kaymer went on to victory that year, over Bubba Watson, if your
        memory fails to serve, as it did for me. (And technically DJ came in T5)<br>
      </p>
      <p>And it seems like eons ago that old man, Rory McIlroy won this
        tournament in handy fashion over Rickie and Phil in 2014. Since then,
        the new guard of Spieth and Rickie are front and center. Although
        Jordan’s opportunity at the grand slam is gone, a win here would
        certainly make a big statement about his place in the game. And perhaps
        DJ might want to make one, too. <br>
      </p>
