     <p>Tiger!! Phil!! Mike Weir!! Well, maybe I should leave Mike off the comeback list, 
      although I do believe he made a cut recently. But nice to see the old-guard announce 
      theyre not through. Phils looked strong all season with an impressive win at the WGC. 
      Tiger  well, what more can be said. As usual, hes ignited the golf world. At least this 
      time its for his remarkable come-back and play. Will that fused-back of his survive? Well see.</p>
      <p>Despite the punters placing Tiger as the odds-on favourite to take this years Masters, I 
      believe some of the top youngsters are going to have a thing to say about it. As long as DJ 
      treads carefully, hell be a strong contender; Rory seems to have gained some form at Arnies 
      tourney. Justin, Jordon, Rickie should be in the mix.</p>
      <p>And congrats to Sergio on the birth of his daughter: Azalea. Ill go out on a limb say he wont repeat 
      if hes got 3:00 AM bottle duty. The picture to the right is a live shot of Serg changing a diaper.</p>
      <p>On a sad note: The pool lost a great supporter a few months ago (and a <b><a id="results" href="Masters2009.html">2009 Masters champion</a></b>), 
      <b><a id="results" href="https://mountpleasantgroup.permavita.com/site/SteveSiuta.html">Steve Siuta</a></b>, 
      chef and co-owner of the Court Jester pub. Despite not being a huge golf fan, hed always enter and promote the 
      pool. And once he told me that he was often so busy, he just entered the default picks! So, in honour of Steve, 
      Ill add the first entry as Court Jester Steve, just the defaults. Any winnings will be donated to the 
      Heart & Stroke foundation.</p>
