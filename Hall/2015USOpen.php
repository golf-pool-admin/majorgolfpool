      <p>The early story to this year’s US Open seems to be the course: Robert
        Trent Jones, Jr’s, Chambers Bay, a links-style course opening for play
        only 8 years ago. It’s the first time the US Open has been contested in
        the Pacific Northwest. So far reviews have been mixed. Ian Poulter seems
        happy to speak for those who think it’s a bad idea; “a complete farce”
        is how Poults put it. Golf writer, Alan Shipnuck called it “jaw
        dropping” and “it can’t get here soon enough” and represents the
        pro-side. Tiger seems to fall within the middle ground, “depends on how
        it’s set up” and “so many options”. I think who ever wins will consider
        this move by the USGA a jolly good idea.<br>
      </p>
      <p>Speaking of Tiger, he flew in a few days ago and got in some practice
        at the course. Earlier, Phil made it in there for a couple of days in
        preparation for his kick at the career-grand-slam can. Rory seems fairly
        confident he can show up and blister the field. Who’s betting he won’t?
        <br>
      </p>
      <p>Well, we can answer that last question with the pool! Rory is firmly on
        top with a lot of the new guard giving chase. Place your wagers, folks!
        Enjoy the pool and US Open, and have a great Father’s Day, too! Fore!<br>
      </p>
