      <h4 id="locationdate" style="text-align: center;">Shinnecock Hills Golf Club, Southampton, N.Y.<br>
        June 14-17, 2018</h4>
     <p>The 2018 US Open returns to Shinnecock Hills, one of the most historic courses in the US and oft cited as 
      one of the top courses in the world. Ive been looking forward to a return to this course for some time. 
      Ben Crenshaws group is the latest architect who made some changes, apparently enhancing the links aspect 
      of the course: less trees, more bunkers, and presumably lengthening the course to test the best golfers in 
      the world. Will be fun to watch.</p>
      <p>And since the Masters we have a new number one in the world, Mr. Justin Thomas. Justin Rose climbs to 3
       in the world after his win at Colonial. And DJ is hanging tough at number 2, although not doing too much 
       lately. Defending champ Koepeka has been also playing surprisingly well after coming back from injury 
       (and after I traded him in another pool, sigh).</p>
      <p>Despite some great golf by the young guns, the media tend to focus on a couple of the more mature set. Expectations for Tiger have come down to earth 
      after his early results but he could always be a factor. And Phil has cooled off, as well, as he tries to complete the career slam. 
      Youre running out of chances, Lefty!</p>
      <p>Great course, great tournament, great golfers  lets have a great pool as well. Enjoy! Fore!!!</p>
