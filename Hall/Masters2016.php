      <p><b id="lnews">Latest Pool News: 2016-04-10 17:39:35</b>Well, Norman-esque. While the rest of us awarded Jordan the green jacket after that 
      putt on 9, Dan Jenkins reminded us that he had yet to play a water hole at AN. Full marks to Jordan making a push after the debacle 
      on 12, with birdies and 13 and 15 and a great tee ball on 16.<br><br>

      I should also congratulate the winner, Danny Willet. Early in the broadcast I was wondering how he made it to 12th in the world. 
      Now we know. Solid play coming in with the lead, and a great final round 67.<br><br>

      To the pool, and Master rooks take the top two spots! Congrats to Ron Nezan and his team nflr's 2 on the pool win at a team score 
      of 3 over par. Ron had Danny, Lee, and DJ as his leading players. Mark Smith's team, Gimmie Par, takes second. Mark also had Danny, 
      DJ, and Casey on his team. Taking third, is Peter Tom's team, Dick's Duffers. Nice work, Peter!<br><br>

