      <p>I'm struggling to find a theme for this golf season, which despite the
        PGA Tour scheduler's attempts, for me always begins in Hawaii. An effort
        seems to be in play to nominate a "big three", presumably Speith, Rory
        and Day. Although #1 Speith has been running hot-and-cold; Rory's only
        shown glimpses of his past swagger; and Rickie want's in as well.</p>
      <p>Bubba has asserted himself early,though; Phil is still hanging in
        there; and DJ is close but can't seem to close. Toss in the usual crop
        of rookies and it's been a real mixed bag. Perhaps anointing the first
        major championship of 2016 will clarify things.<br>
      </p>
      <p>Welcome to spring '16. Looking forward to the pines and azaleas from
        Augusta, and hopefully some exciting drama as well. The pool is up! Get
        your picks in now! Fore!</p>
